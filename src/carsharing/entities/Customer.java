package carsharing.entities;

public class Customer {
    private int id;
    private String name;
    private int carId;
    
    Customer() {}

    public Customer(int id, String name, int carId) {
        this.id = id;
        this.name = name;
        this.carId = carId;
    }

    public Customer(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCarId() {
        return carId;
    }

    public void setCarId(int carId) {
        this.carId = carId;
    }

    @Override
    public String toString() {
        return "Customer{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
